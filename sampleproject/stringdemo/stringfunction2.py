# starts with
str1="Bangalore is a garden city of Karnataka"
print(str1.startswith("Bangalore"))

# ends With
str2="Bangalore is a garden city of Karnataka"
print(str2.endswith("Karnataka"))

#count
str3="Welcome Welcome welcome Welcome welcome"
print(str3.count("welcome"))
print(str3.count("Welcome"))

# find
str4="It is a new book, it is on the table"
print(str4.find("is"))

# rfind
str5="It is a new book, it is on the table"
print(str5.rfind("is"))